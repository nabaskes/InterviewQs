-- 4 	5 	Online 	2018-08-13 	08 	200
-- 5 	6 	Online 	2018-10-29 	10 	100

-- Upgrade to premium to receive in-depth solutions to each problem.

SELECT month, channel, avg(revenue)
FROM orders
GROUP BY month, channel
